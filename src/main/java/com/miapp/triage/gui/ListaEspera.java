    /**
     * carga datos de pacientes a la tabla
     * 
     * @return datos 
     */
    private void cargarDatosDesdeCSV() {
    DefaultTableModel modeloTabla = (DefaultTableModel) jTable3.getModel();
    modeloTabla.setRowCount(0); 

    try {
        BufferedReader brPacientes = new BufferedReader(new FileReader("src\\main\\java\\com\\miapp\\triage\\csv\\pacientes.csv"));
        BufferedReader brTriage = new BufferedReader(new FileReader("src\\main\\java\\com\\miapp\\triage\\csv\\triage.csv"));
        String lineaPacientes;
        String lineaTriage;

        while ((lineaPacientes = brPacientes.readLine()) != null && (lineaTriage = brTriage.readLine()) != null) {
            String[] datosPacientes = lineaPacientes.split(";");
            String[] datosTriage = lineaTriage.split(";");
            
            if (datosPacientes.length >= 3 && datosTriage.length >= 4) {
                String nombre = datosPacientes[1]; 
                String apellido = datosPacientes[2];
                String DNI = datosPacientes[5];
                String urgencia = datosTriage[1]; 
                String tiempo = datosTriage[3]; 

                // Crea una fila que contenga todos los datos y agr√©gala al JTable
                modeloTabla.addRow(new Object[] {nombre, apellido, DNI, urgencia, tiempo});
                
            }
        }
        
        brPacientes.close();
        brTriage.close();
    } catch (IOException e) {
        e.printStackTrace();
    }
}


    /**
     * obtiene dni desde la tabla
     * 
     * @return dni
     */
     public List<String> obtenerDniDesdeTabla() {
    DefaultTableModel modeloTabla = (DefaultTableModel) jTable3.getModel();
    List<String> listaDNI = new ArrayList<>();

    for (int fila = 0; fila < modeloTabla.getRowCount(); fila++) {
        String dni = (String) modeloTabla.getValueAt(fila, 2); 
        listaDNI.add(dni);
    }

    return listaDNI;
}
     
